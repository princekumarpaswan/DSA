public class Wile {
    public static void main(String[] args) {

        // While loop
        int count = 1;
        while (count != 5) {
            System.out.println("Value of count" + count);
            count++;
        }


        // for loop
        for (int cot = 1; cot != 5; cot++) {
            System.out.println("For loop" + cot);
        }
    }
}
